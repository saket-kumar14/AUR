  id: string;
  name: string;
  location: string;
  overall: number; // calculated score out of 100
  citations: number; // 0-100
  employability: number; // 0-100
  intlStudents: number; // 0-100
  teaching: number; // 0-100
  research: number; // 0-100
  academicReputation?: number;
  employerReputation?: number;
  facultyStudentRatio?: number;
  subjects: string[];
  languages: string[];
  tuition: string; // e.g. "$3,500/year"
  description: string;
  history: number[]; // 5-year rank history (from current year back to 5 years ago, e.g., 2026, 2025, 2024, 2023, 2022)
  programs: string[];
  campusPhoto: string; // local path to campus photo
  logo?: string; // local path to university logo
  website?: string;
  hasMedicine: boolean;
  qsSubjectRankings?: {
    subject: string;
    worldRank: string;
    score: number;
  }[];
  // Optional flags used by filters. When missing, UI falls back to legacy ID-based rules.
  isPublic?: boolean;
  hasScholarship?: boolean;
  // ── Expanded admission-depth fields (PRD item 4) ──
  founded?: number;           // year the university was established
  studentCount?: number;      // total enrolled students (approx)
  facultyCount?: number;      // total academic staff
  acceptanceRate?: number;    // percentage 0-100
  applicationDeadline?: string; // e.g. "March 31, 2027"
